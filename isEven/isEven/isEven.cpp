#include <iostream>

int main()
{
    int num;
    std::cout << "Enter your number: \n";
    std::cin >> num;

    if (num % 2 == 0) {
        std::cout << "Your number is even.\n";
    }
    else {
        std::cout << "Your number is odd.\n";
    }

    return 0;
};



